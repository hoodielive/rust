pub fn run()
{
    let age: u8 = 18;
    let _check_id: bool = false;


    //If/Else
    if age >= 21 && check_id
    {
        println!("Bartender: What would you like to drink?");
    }
    else if age < 21 && check_id {
        println!("You are not old enough to drink.");
    }
    else {
        
        println!("I'll need to see your ID.");
    }
}
